'''Escribir los números del 0 al 10'''

for numero in range(11):
    print(f'{numero}')
else:
    print('Terminamos.')

'''Escribir los números del 5 al 10'''

for numero in range(5,11):
    print(f'{numero}')
else:
    print('Terminamos.')

'''Escribir los números pares del 2 al 20'''

for par in range(2,21,2):
    print(par)
else:
    print('Terminamos.')